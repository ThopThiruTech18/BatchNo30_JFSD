package no01.versionit.nestedforloops;

public class Pattern5 {

	public static void main(String[] args) {
		
		System.out.println("Starting the pattern");

		for (char i = 'e'; i >= 'a'; i--) {

			for (char j = 'a'; j <= 'e'; j++) {

				System.out.print(i);

			}

			System.out.println();

		}

	}

}
