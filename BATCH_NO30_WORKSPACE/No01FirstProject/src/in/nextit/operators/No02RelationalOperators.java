package in.nextit.operators;

public class No02RelationalOperators {

	public static void main(String[] args) {

//		 2) Relational Operators/Comparison Operator[==,!=,<,<=,>,>=]
		int a = 10;
		int b = 20;
		System.out.println(a == b); // false
		System.out.println(a != b); // true
		System.out.println(a < b); // true
		System.out.println(a <= b);// true
		System.out.println(a > b);// false
		System.out.println(a >= b);// false

	}

}
